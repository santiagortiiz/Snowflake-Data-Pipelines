-- Store procedure for lineitems
CREATE OR REPLACE PROCEDURE move_customer_stage_to_dwh()
returns varchar
language sql
as
$$
begin
    INSERT INTO EPAM_LAB.CORE_DWH.LINEITEM (
        l_orderkey,
        l_partkey,
        l_suppkey,
        l_linenumber,
        l_quantity,
        l_extendedprice,
        l_discount,
        l_tax,
        l_returnflag,
        l_linestatus,
        l_shipdate,
        l_commitdate,
        l_receiptdate,
        l_shipinstruct,
        l_shipmode,
        l_comment
    )
    SELECT
        l_orderkey,
        l_partkey,
        l_suppkey,
        l_linenumber,
        l_quantity,
        l_extendedprice,
        l_discount,
        l_tax,
        l_returnflag,
        l_linestatus,
        l_shipdate,
        l_commitdate,
        l_receiptdate,
        l_shipinstruct,
        l_shipmode,
        l_comment
    FROM EPAM_LAB.PUBLIC.LINEITEM_CHECK
    WHERE metadata$action = 'INSERT';

    return 'Success';
end;
$$
;